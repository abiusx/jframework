<title>jFramework Features</title>
<h1>jFramework Features</h1>
<p>
You might wonder why you should switch to jFramework (or start using it as your first framework) 
when there are plenty of other frameworks with some benefits our there. Here's a list of 
what jFramework can offer:

<ul>
<li>
	<strong>Libraries : </strong> a set of libraries do common web application tasks for you.
	 These are:
	 <ul>
	 	<li><strong>DBAL</strong> which is a database abstraction focused on Performance and Simplicity</li>
	 	<li><strong>Log</strong> which is a simple and fast logging facility</li>
	 	<li><strong>Options</strong> which perfectly provides you with persistent data save/load over a Session,
	 	a User Account or the whole application</li>
	 	<li><strong>RBAC</strong> which is the cutting-edge Role Based Access Control mechanism</li>
	 	<li><strong>Security</strong> which provides you with lethal security requirements of a web</li>
	 	<li><strong>Session</strong> which is a pretty fast and flexible session and user management library</li>
	 	<li><strong>Tracker</strong> which provides the developer with benchmarking and profiling</li>
	 </ul>
</li>
<li><strong>Application Controller : </strong> a central starting point for the framework,
which initializes least necessary code but provides you with lots of functionality.</li>

<li><strong>jPath : </strong> an abstraction layer over different paths, which usually bug developers
of a web application.</li>
<li><strong>Administration Web Interface : </strong> provides the administrator of a jFramework
web site or even the developer with lots of visual reports and facilities to handle different
aspects of the deployed framework installation.</li>

<li><strong>Plugins : </strong> allow reuse of every piece of convenient code in your jFramework application,
vastly easy.</li>
<li><strong>Patterns : </strong> jFramework supports almost all basic programming patterns. You can
use MVC or procedural or flat or service oriented or object oriented. Whatever you like. jFramework
does not force you to follow particular syntax and notation, but recommends one.</li>
<li><strong>Services : </strong> allow your jFramework application to consume and provide web services
of wide standards, in the smartest and simplest abstracted way possible.</li>
<li><strong>j Helper : </strong> makes your web development experience much much sweeter by providing
useful shortcuts to most of what you need.</li>
<li><strong>Error Handling : </strong> helps developer debug the application much easier.</li>
<li><strong>Fileman : </strong> an incredibly creative interface for providing application users with
file resources, with lots of functionalities.</li>
<li><strong>jURL : </strong> provides developer and admin with easy-to-use URL understanding and manipulation.</li>
<li><strong>Registry : </strong> a centralized neat repository for constants and application/framework specific configurations.</li>
<li><strong>Base Classes : </strong> help developer skip lots of code but inherit lots of functionality!</li>
<li><strong>Configurations : </strong> you can tweak 100% of jFramework without understanding a single line of its code, 
ordered in a logical breadthing manner.</li>
<li><strong>Templates : </strong> manage views with ease you couldn't imagine.</li>
<li><strong>View Parser : </strong> fixes URLs and performs output cleanings and optimization.</li>
</ul>
And a lot more... For a detailed descriptiong of the above features, visit <a href='http://wiki.jframework.info'>jFramework Wiki</a>.

</p>
